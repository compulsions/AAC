				  	<img style="width:30%;" src="img/organismos/citac.png"  onerror="this.src='notfound.png'" alt="organismo autonomo">
				  	<p>O Círculo de Iniciação Teatral da Academia de Coimbra, CITAC, é um organismo Autónomo da AAC (Associação Académica de Coimbra), orientado no sentido da criação prática e formação no domínio do teatro. Nascido em 1954, adopta o desafio do experimentalismo. Além de um grupo de teatro, podemos destacar o seu carácter de resistência ao fascismo anterior à revolução de Abril, razão pela qual foi perseguido e, posteriormente, fechado pela PIDE (1970). Desde então tem traçado um caminho, resistindo a moldes pré-concebidos, através de uma seleção eclética de textos e da sua adaptação para o palco pela interação de elementos técnicos e humanos, possível pelo entendimento mútuo de todos os intervenientes que contribuem como um todo para a conceção do espetáculo.</p>
					<p>É nesta perspetiva que o CITAC procura definir e assumir a sua identidade, desenvolvendo ao longo destes 50 anos de existência uma batalha constante para o enriquecimento e dignificação do Teatro Universitário em Portugal. A sua actividade tem-se baseado na produção de espectáculos teatrais, digressões no país e no estrangeiro, bem como na participação em vários festivais de teatro. Organizou festivais, editou os “Cadernos de Teatro” em continuidade do “Boletim do CITAC”, publicações no campo teatral. No entanto, o seu principal objectivo foi sempre a formação, apostando para isso num curso de iniciação teatral que se realiza bianualmente e em ateliers específicos para a aprendizagem de determinadas técnicas.</p>
					<p>Paralelamente a estas actividades é hábito do CITAC manter a realização de eventos culturais nas suas mais variadas vertentes, desde concertos, exposições de arte, workshops vários, e performances de rua, entre outros, intervindo pontualmente em eventos que aconteçam na cidade. </p>
					<p>Tudo se tem tornado possível neste nosso círculo pela alternância de pessoas interessadas em fazer teatro, em experimentar o crescimento pessoal através da arte. Umas seguem o caminho do teatro, a maioria guarda-o consigo.</p>	
